for d in $(ls -d * | grep -v "\."); do
    echo "--------------------------------------------------------------------------------";
    echo "Now Running Example: " $d;
    echo "--------------------------------------------------------------------------------";
    cd $d;
    go run *.go;
    cd ..;
    echo "--------------------------------------------------------------------------------";
    echo "Finished Example: " $d;
    echo "--------------------------------------------------------------------------------";
    echo;
    echo;
done 2>&1 | tee all.log | less -S
